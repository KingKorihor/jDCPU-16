package org.sini;

/**
 * Cell.java
 * @version 1.0.0
 */
public final class Cell {
    
    /**
     * The value of this cell.
     */
    int v;
    
    /**
     * The number of this cell.
     */
    int n;
    
    /**
     * Constructs a new {@link Cell};
     * @param n The cell number.
     */
    public Cell(int n) {
        this.n = n;
    } 
}
